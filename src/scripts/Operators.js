// Operators
// == toma valor, ignora el tipo
// === conversion al mismo tipo de dato y valida
// != valida contenido
// !== conversion al mismo tipo y valida valor
// <<= =>> mueve bits dos puntos a la izquierda o derecha
console.log(1 == 1);
console.log(1 == '1');
console.log(1 === '1');
console.log(1 != '1');
console.log(1 !== '1');

let year = 1994;
console.log(year);
year <<= 2;
console.log(year);
year >>= 2;
console.log(year);
